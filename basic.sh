#!/bin/bash
#Between double quotes or single quotes. It will print the same.

echo "Hello   Learner"     #It will print msg with given spaces.

echo "Hello Learner"       #It will print the same.

echo "Hello * Learner"    #It will print the same.

echo 'Hello Learner * Hello Learner'       #It will print the same.

echo Hello * Learner     #It will print the content of the present directory.Ex: Hello content Learner

echo "Hello "*" Learner"    #It will print Hello * Learner.Because double quotes hold it * (asterisk) at this time.

echo `command` learner      # between this  `---`(back-tic) must be a command.

